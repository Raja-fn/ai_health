import 'package:flutter/material.dart';
import 'package:health_connector/health_connector_internal.dart';
import 'package:ai_health/src/common/constants/app_icons.dart';
import 'package:ai_health/src/common/constants/app_texts.dart';
import 'package:ai_health/src/common/utils/extensions/cervical_mucus_appearance_extension.dart';
import 'package:ai_health/src/common/utils/extensions/cervical_mucus_sensation_extension.dart';
import 'package:ai_health/src/features/read_health_records/widgets/health_record_list_tiles/health_record_list_tile_subtitle.dart';
import 'package:ai_health/src/features/read_health_records/widgets/health_record_list_tiles/instant_health_record_list_tiles/instant_health_record_list_tile.dart';


final class CervicalMucusListTile extends StatelessWidget {
  const CervicalMucusListTile({
    required this.record,
    required this.onDelete,
    super.key,
  });

  final CervicalMucusRecord record;
  final VoidCallback onDelete;

  @override
  Widget build(BuildContext context) {
    final appearanceText = record.appearance.displayName;
    final sensationText = record.sensation.displayName;

    return InstantHealthRecordTile<CervicalMucusRecord>(
      record: record,
      icon: AppIcons.waterDrop,
      title: AppTexts.cervicalMucus,
      subtitleBuilder: (r, ctx) => HealthRecordListTileSubtitle.instant(
        time: r.time,
        recordingMethod: r.metadata.recordingMethod.name,
      ),
      detailRowsBuilder: (r, ctx) => [
        _DetailRow(
          label: AppTexts.appearance,
          value: appearanceText,
        ),
        _DetailRow(
          label: AppTexts.sensation,
          value: sensationText,
        ),
      ],
      onDelete: onDelete,
    );
  }
}


class _DetailRow extends StatelessWidget {
  const _DetailRow({
    required this.label,
    required this.value,
  });

  final String label;
  final String value;

  @override
  Widget build(BuildContext context) {
    return Padding(
      padding: const EdgeInsets.symmetric(vertical: 4),
      child: Row(
        mainAxisAlignment: MainAxisAlignment.spaceBetween,
        children: [
          Text(
            label,
            style: Theme.of(context).textTheme.bodyMedium?.copyWith(
              color: Theme.of(context).colorScheme.onSurfaceVariant,
            ),
          ),
          Text(
            value,
            style: Theme.of(context).textTheme.bodyMedium?.copyWith(
              fontWeight: FontWeight.w500,
            ),
          ),
        ],
      ),
    );
  }
}
