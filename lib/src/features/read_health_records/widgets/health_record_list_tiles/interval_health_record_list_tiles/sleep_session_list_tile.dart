import 'package:flutter/material.dart';
import 'package:health_connector/health_connector_internal.dart';
import 'package:ai_health/src/common/constants/app_icons.dart';
import 'package:ai_health/src/common/constants/app_texts.dart';
import 'package:ai_health/src/features/read_health_records/widgets/health_record_list_tiles/series_health_record_list_tiles/series_health_record_list_tile.dart';
import 'package:ai_health/src/features/read_health_records/widgets/sleep_session_record_sleep_stages_list.dart';


final class SleepSessionTile extends StatelessWidget {
  const SleepSessionTile({
    required this.record,
    required this.onDelete,
    super.key,
  });

  final SleepSessionRecord record;
  final VoidCallback onDelete;

  @override
  Widget build(BuildContext context) {
    return SeriesHealthRecordTile<SleepSessionRecord, SleepStageSample>(
      record: record,
      icon: AppIcons.bedtime,
      title: 'Sleep Session',
      subtitleBuilder: (r, ctx) => Text(
        '${AppTexts.recording}: ${r.metadata.recordingMethod.name}',
        style: Theme.of(ctx).textTheme.bodySmall,
      ),
      detailRowsBuilder: (r, ctx) => [],
      samplesBuilder: (samples, ctx) =>
          SleepSessionRecordSleepStagesList(stages: samples),
      onDelete: onDelete,
    );
  }
}
